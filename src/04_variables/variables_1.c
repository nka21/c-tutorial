#include <stdio.h>
int main() {

    /*
      変数とは =  変数は、特定の型のデータを格納するためにメモリ内に割り当てられたスペース。
                変数を使用する前に、格納するデータの型を宣言する必要がある。
                変数には名前が付けられ、その名前を使って格納された値にアクセス。
                プログラム内で変数を使用すると、それが含む値として扱われる。
                変数の値は、プログラムの実行中に変更することができる。
    */
    int x;       // 数値型の変数xを宣言
    x = 123;     // 変数xの値を、123と初期化
    int y = 123; // 宣言と初期化を同時に行う

    return 0;
}
